export function getFrenchArticleBySlug(slug: string): BlogArticle | undefined {
  return frenchBlogArticles.find(article => article.slug === slug);
}

export function getPublishedFrenchArticles(): BlogArticle[] {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  return frenchBlogArticles.filter(article => {
    const publishDate = new Date(article.publishDate || article.datePublished);
    publishDate.setHours(0, 0, 0, 0);
    return publishDate <= today;
  });
}
